public class Person_Task_1 {
    String name;
    int age;
    String city;

    Person_Task_1(String name, int age, String city) {
        this.name = name;
        this.age = age;
        this.city = city;
    }

    public String toString() {
        return "Name: " + name + ", Age: " + age + ", City: " + city;
    }
}
